package ch01;

public class LongType {

	public static void main(String[] args) {
		// long 타입은 64bit로 int 타입보다 크다 32bit

		long var1 = 10 ;
		long var2 = 20L ;
		//long ver3 = 100000000 L를 붙여야함.
		long var4 = 10000000L ;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var4);
				
		
		
		
		
	}

}
